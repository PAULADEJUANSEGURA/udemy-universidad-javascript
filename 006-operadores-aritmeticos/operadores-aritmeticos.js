/* Los operadores aritmeticos son:
Suma +
Resta -
Multiplicacion *
Division /
Modulo %
Potencia **
Incremento ++
Decremento --
*/

// SUMA
let miNumero1 = 25;
let miNumero2 = 5;
let resultado = miNumero1 + miNumero2;
console.log(resultado);

// RESTA
resultado = miNumero1 - miNumero2;
console.log(resultado);

// MULTIPLICACION
resultado = miNumero1 * miNumero2;
console.log(resultado);


// DIVISION
resultado = miNumero1 / miNumero2;
console.log(resultado);

// MODULOS
resultado = miNumero1 % miNumero2;
console.log(resultado);

// POTENCIA
resultado = miNumero1 ** miNumero2;
console.log(resultado);

